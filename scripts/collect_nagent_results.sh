#!/bin/bash
# =============================================================================
# Collect and Aggregate Results from N-Agent Scaling Experiments
# =============================================================================
#
# Walks through N-agent experiment output directories, extracts results
# from experiment_results.json files, and aggregates them into CSV + JSON
# for downstream analysis and visualization.
#
# Supports all 3 game types (Item Allocation, Diplomacy, Co-Funding) and
# handles variable numbers of agents (N=2..5).
#
# Usage:
#   ./scripts/collect_nagent_results.sh                         # Use nagent_latest symlink
#   ./scripts/collect_nagent_results.sh nagent_20260306_123456  # Specific experiment dir
#
# What it creates:
#   experiments/results/<nagent_dir>/
#   ├── all_results.json       # Full result data for all experiments
#   ├── results_summary.json   # Aggregate statistics
#   └── results.csv            # Flat CSV for pandas/R analysis
#
# Dependencies:
#   - python3 with json, csv, pathlib (stdlib only)
#   - Experiment results must be generated by run_strong_models_experiment.py
#
# =============================================================================

set -e

BASE_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

# Determine which experiment directory to collect from
if [[ -n "$1" ]]; then
    EXPERIMENT_NAME="$1"
else
    EXPERIMENT_NAME="nagent_latest"
fi

RESULTS_DIR="${BASE_DIR}/experiments/results/${EXPERIMENT_NAME}"

if [[ ! -d "${RESULTS_DIR}" ]]; then
    echo "ERROR: Results directory not found: ${RESULTS_DIR}"
    echo "Available N-agent experiment directories:"
    ls -d "${BASE_DIR}/experiments/results/nagent_"* 2>/dev/null || echo "  (none found)"
    exit 1
fi

echo "Collecting N-agent results from: ${RESULTS_DIR}"
echo ""

# Run Python aggregation script
export BASE_DIR RESULTS_DIR
python3 << 'PYEOF'
import json
import csv
import os
import sys
from pathlib import Path
from collections import defaultdict

results_dir = Path(os.environ["RESULTS_DIR"])
base_dir = Path(os.environ["BASE_DIR"])
config_dir = results_dir / "configs"

all_results = []
successful = 0
failed = 0
in_progress = 0
not_started = 0

# Process each configuration
config_files = sorted(config_dir.glob("config_*.json"))
if not config_files:
    print("ERROR: No config files found in", config_dir)
    sys.exit(1)

print(f"Found {len(config_files)} config files")

# Track max agents seen for dynamic column generation
max_agents = 0

for config_file in config_files:
    job_id = config_file.stem.split("_")[1]

    with open(config_file) as f:
        config = json.load(f)

    output_dir = base_dir / config["output_dir"]
    game_type = config.get("game_type", "")
    models = config.get("models", [])
    n_agents = config.get("n_agents", len(models))
    max_agents = max(max_agents, n_agents)

    # Look for experiment result files
    result_file = None
    for pattern in [
        "run_*_experiment_results.json",
        "experiment_results.json",
    ]:
        candidates = list(output_dir.glob(pattern))
        if candidates:
            result_file = candidates[0]
            break

    row = {
        "experiment_id": config["experiment_id"],
        "experiment_type": config.get("experiment_type", ""),
        "experiment_design": config.get("experiment_design", ""),
        "game_type": game_type,
        "models": "+".join(models),
        "n_agents": n_agents,
        "random_seed": config.get("random_seed", 0),
        "run_number": config.get("run_number", 1),
        "config_file": config_file.name,
    }

    # Add game-specific parameters
    if game_type == "item_allocation":
        row["competition_level"] = config.get("competition_level", "")
        row["num_items"] = config.get("num_items", 5)
    elif game_type == "diplomacy":
        row["rho"] = config.get("rho", "")
        row["theta"] = config.get("theta", "")
        row["n_issues"] = config.get("n_issues", 5)
    elif game_type == "co_funding":
        row["alpha"] = config.get("alpha", "")
        row["sigma"] = config.get("sigma", "")
        row["m_projects"] = config.get("m_projects", 5)

    # Add per-agent model info
    for i, model in enumerate(models):
        row[f"model_agent_{i}"] = model

    if result_file is not None and result_file.exists():
        try:
            with open(result_file) as f:
                result = json.load(f)

            row["status"] = "SUCCESS"
            row["consensus_reached"] = result.get("consensus_reached", False)
            row["final_round"] = result.get("final_round", -1)

            # Extract utilities (N agents)
            utilities = result.get("final_utilities", {})
            for agent_id, util in utilities.items():
                row[f"utility_{agent_id}"] = util

            # Game-specific result fields
            if game_type == "co_funding":
                funded = result.get("final_allocation", [])
                if isinstance(funded, list):
                    row["num_funded"] = len(funded)
                    row["funded_projects"] = json.dumps(funded)
                else:
                    row["num_funded"] = 0
                    row["funded_projects"] = "[]"
            elif game_type in ("item_allocation", "diplomacy"):
                alloc = result.get("final_allocation", {})
                row["final_allocation"] = json.dumps(alloc)

            # Extract preferences
            prefs = result.get("agent_preferences", {})
            for agent_id, pref_vals in prefs.items():
                row[f"preferences_{agent_id}"] = json.dumps(pref_vals)

            # Store full result for JSON output
            row["_full_result"] = result
            successful += 1

        except (json.JSONDecodeError, KeyError) as e:
            row["status"] = "PARSE_ERROR"
            row["error"] = str(e)
            failed += 1
    else:
        if output_dir.exists():
            row["status"] = "IN_PROGRESS"
            in_progress += 1
        else:
            row["status"] = "NOT_STARTED"
            not_started += 1

    all_results.append(row)

# Compute summary statistics
total = len(all_results)
success_rate = successful / total if total > 0 else 0

summary = {
    "total": total,
    "successful": successful,
    "failed": failed,
    "in_progress": in_progress,
    "not_started": not_started,
    "success_rate": success_rate,
    "max_agents": max_agents,
    "by_experiment_type": defaultdict(lambda: {"total": 0, "successful": 0}),
    "by_game_type": defaultdict(lambda: {"total": 0, "successful": 0}),
    "by_n_agents": defaultdict(lambda: {"total": 0, "successful": 0}),
}

for row in all_results:
    for group_key, row_key in [
        ("by_experiment_type", "experiment_type"),
        ("by_game_type", "game_type"),
        ("by_n_agents", "n_agents"),
    ]:
        key = str(row[row_key])
        summary[group_key][key]["total"] += 1
        if row.get("status") == "SUCCESS":
            summary[group_key][key]["successful"] += 1

# Convert defaultdicts for JSON serialization
for k in ("by_experiment_type", "by_game_type", "by_n_agents"):
    summary[k] = dict(summary[k])

# Save all_results.json
all_results_json = []
for row in all_results:
    json_row = {k: v for k, v in row.items() if k != "_full_result"}
    full_result = row.get("_full_result")
    if full_result:
        json_row["result"] = full_result
    all_results_json.append(json_row)

all_results_file = results_dir / "all_results.json"
with open(all_results_file, "w") as f:
    json.dump(all_results_json, f, indent=2, default=str)

# Save summary
summary_file = results_dir / "results_summary.json"
with open(summary_file, "w") as f:
    json.dump(summary, f, indent=2)

# Save CSV (flat, no nested objects)
# Build dynamic field list
base_fields = [
    "experiment_id", "experiment_type", "experiment_design", "game_type",
    "models", "n_agents", "random_seed", "run_number", "status",
    "consensus_reached", "final_round",
]

# Game-specific fields
game_fields = [
    "competition_level", "num_items",
    "rho", "theta", "n_issues",
    "alpha", "sigma", "m_projects",
    "num_funded",
]

# Per-agent model columns
model_cols = [f"model_agent_{i}" for i in range(max_agents)]

# Dynamic utility columns
utility_cols = set()
for row in all_results:
    for k in row:
        if k.startswith("utility_"):
            utility_cols.add(k)

csv_fields = base_fields + game_fields + model_cols + sorted(utility_cols)

csv_file = results_dir / "results.csv"
with open(csv_file, "w", newline="") as f:
    writer = csv.DictWriter(f, fieldnames=csv_fields, extrasaction="ignore")
    writer.writeheader()
    for row in all_results:
        csv_row = {k: row.get(k, "") for k in csv_fields}
        writer.writerow(csv_row)

print(f"Collected {total} experiment results:")
print(f"  Successful:  {successful}")
print(f"  Failed:      {failed}")
print(f"  In progress: {in_progress}")
print(f"  Not started: {not_started}")
print(f"  Success rate: {success_rate*100:.1f}%")
print()
print(f"Breakdown by experiment type:")
for etype, counts in sorted(summary["by_experiment_type"].items()):
    print(f"  {etype}: {counts['successful']}/{counts['total']}")
print()
print(f"Breakdown by game type:")
for gtype, counts in sorted(summary["by_game_type"].items()):
    print(f"  {gtype}: {counts['successful']}/{counts['total']}")
print()
print(f"Breakdown by N agents:")
for n, counts in sorted(summary["by_n_agents"].items()):
    print(f"  N={n}: {counts['successful']}/{counts['total']}")
print()
print(f"Results saved to:")
print(f"  JSON:    {all_results_file}")
print(f"  Summary: {summary_file}")
print(f"  CSV:     {csv_file}")
PYEOF

echo ""
echo "Done! To visualize results:"
echo "  python3 visualization/visualize_nagent.py --results-dir ${RESULTS_DIR}"
